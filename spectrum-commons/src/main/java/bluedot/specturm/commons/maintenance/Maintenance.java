package bluedot.specturm.commons.maintenance;

/**
 * 暴露出的维护模块抽象类
 * @author Harold
 * @date 2018年3月29日 
 * @description:
 */
public abstract class Maintenance {
	/**
	 * 数据库恢复
	 * @return 返回恢复的结果
	 */
	public abstract boolean databaseRecover(String filePath);
	
	/**
	 * 数据库备份
	 * @return 返回备份的路径
	 */
	public abstract String databaseBackup(String path);
}
